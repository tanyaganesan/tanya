%**************************************************************************
% Determines soil element matrix
%**************************************************************************
function [kesL1,kesL2,kesR1,kesR2] = soil_sitff(le,t1,k1,t3,k3)
%**************************************************************************
kes1(1,1) = Gaussinf(@(x)1/le^2);
kes1(1,2) = Gaussinf(@(x)-1/le^2);
%**************
kes1(2,1) = Gaussinf(@(x)-1/le^2);
kes1(2,2) = Gaussinf(@(x)1/le^2);
%**************************************************************************
kesL1 = 2 * t1 * le/2 * kes1;
%**************************************************************************
kes2(1,1) = Gaussinf(@(x)(1-x)^2/4);
kes2(1,2) = Gaussinf(@(x)(1+x)*(1-x)/4);
%**************
kes2(2,1) = Gaussinf(@(x)(1+x)*(1-x)/4);
kes2(2,2) = Gaussinf(@(x)(1+x)^2/4);
%**************************************************************************
kesL2 = k1 * le/2 * kes2;
%**************************************************************************
kes3(1,1) = Gaussinf(@(x)1/le^2);
kes3(1,2) = Gaussinf(@(x)-1/le^2);
%**************
kes3(2,1) = Gaussinf(@(x)-1/le^2);
kes3(2,2) = Gaussinf(@(x)1/le^2);
%**************************************************************************
kesR1 = 2 * t3 * le/2 * kes3;
%**************************************************************************
kes4(1,1) = Gaussinf(@(x)(1-x)^2/4);
kes4(1,2) = Gaussinf(@(x)(1+x)*(1-x)/4);
%**************
kes4(2,1) = Gaussinf(@(x)(1+x)*(1-x)/4);
kes4(2,2) = Gaussinf(@(x)(1+x)^2/4);
%**************************************************************************
kesR2 = k3 * le/2 * kes4;
%**************************************************************************
