%**************************************************************************
% Determines Load Vector
%**************************************************************************
function [qb,qs] = load_vector(le,Qs,Q)
%**************************************************************************
% beam
Nb(1,1) = Gaussinf(@(x)(1/4)*(1-x)^2*(2+x));
Nb(2,1) = Gaussinf(@(x)(le/8)*(1-x)^2*(1+x));
Nb(3,1) = Gaussinf(@(x)(1/4)*(1+x)^2*(2-x));
Nb(4,1) = Gaussinf(@(x)-(le/8)*(1+x)^2*(1-x));
%**************************************************************************
qb = Q * le/2 * Nb;
%**************************************************************************
% soil
Ns(1,1) = Gaussinf(@(x)(1-x)/2);
Ns(2,1) = Gaussinf(@(x)(1+x)/2);
%**************************************************************************
qs = Qs * le/2 * Ns;
return